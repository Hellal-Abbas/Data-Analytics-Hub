package model;

//This class represents a model for storing all username information.
public class AllUsersModel {

	String username;

	// Constructor for initializing the AllUsersModel object with a username.
	public AllUsersModel(String username) {
		this.username = username;
	}

	// Getter method to retrieve the username of the user.
	public String getUsername() {
		return username;
	}

	// Setter method to update the username with a new value.
	public void setUsername(String datetime) {
		this.username = datetime;
	}

}
